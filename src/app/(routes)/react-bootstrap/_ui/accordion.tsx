"use client";

//? REACT BOOTSTRAP
import Accordion from "react-bootstrap/Accordion";
import { useAccordionButton } from "react-bootstrap/AccordionButton";
import Card from "react-bootstrap/Card";

//? TS Props for customToggle
interface CustomToggleProps {
  //* Specifies that children can be any valid React node
  children: React.ReactNode;
  //* Ensures eventKey is a string, required by useAccordionButton
  eventKey: string;
}

//? Custom toggle component for controlling the Accordion
function CustomToggle({ children, eventKey }: CustomToggleProps) {

  const decoratedOnClick = useAccordionButton(eventKey, () =>
    //* Logs a message when the button is clicked
    console.log('totally custom!'),
  );

  return (
    <button
      type="button"
      style={{ backgroundColor: 'white' }}
      onClick={decoratedOnClick}
    >
      {children}
    </button>
  );
}

export function AccordionExample() {
  return (
    <Accordion defaultActiveKey="0">
      <Card>
        <Card.Header>
          <CustomToggle eventKey="0">Header/Title 1</CustomToggle>
        </Card.Header>
        <Accordion.Collapse eventKey="0">
          <Card.Body>Hello! I'm the body</Card.Body>
        </Accordion.Collapse>
      </Card>
      <Card>
        <Card.Header>
          <CustomToggle eventKey="1">Header/Title 2</CustomToggle>
        </Card.Header>
        <Accordion.Collapse eventKey="1">
          <Card.Body>Hello! I'm another body</Card.Body>
        </Accordion.Collapse>
      </Card>
    </Accordion>
  );
}

